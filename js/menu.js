function openMenu() {
    document.getElementById("fas").style.display = "none";
    document.getElementById("far").style.display = "block";
    document.getElementById("menu-icon").style.marginTop = "5px";
    document.getElementById("marn").style.display = "none";

  }
  
  function closeMenu() {
    document.getElementById("far").style.display = "none";
    document.getElementById("fas").style.display = "block";
    document.getElementById("marn").style.display = "block";

  }

